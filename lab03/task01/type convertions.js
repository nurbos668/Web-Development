"" + 1 + 0
// "" + 1  → "1"   (строка + число = строка)
// "1" + 0 → "10"
// результат: "10"


"" - 1 + 0
// "" → 0   (при минусе строка приводится к числу)
// 0 - 1 = -1
// -1 + 0 = -1
// результат: -1


true + false
// true → 1
// false → 0
// 1 + 0 = 1
// результат: 1


6 / "3"
// "3" → 3
// 6 / 3 = 2
// результат: 2


"2" * "3"
// "2" → 2
// "3" → 3
// 2 * 3 = 6
// результат: 6


4 + 5 + "px"
// 4 + 5 = 9
// 9 + "px" → "9px"
// результат: "9px"


"$" + 4 + 5
// "$" + 4 → "$4"
// "$4" + 5 → "$45"
// результат: "$45"


"4" - 2
// "4" → 4
// 4 - 2 = 2
// результат: 2


"4px" - 2
// "4px" нельзя превратить в число → NaN
// NaN - 2 = NaN
// результат: NaN


"  -9  " + 5
// строка + число = строка
// результат: "  -9  5"


"  -9  " - 5
// строка "  -9  " → -9
// -9 - 5 = -14
// результат: -14


null + 1
// null → 0
// 0 + 1 = 1
// результат: 1


undefined + 1
// undefined → NaN
// NaN + 1 = NaN
// результат: NaN


" \t \n" - 2
// строка с пробелами, табами, переносами → 0
// 0 - 2 = -2
// результат: -2
