5 > 4
// обычное сравнение чисел
// 5 больше 4
// результат: true


"apple" > "pineapple"
// строки сравниваются ПОСИМВОЛЬНО (лексикографически)
// 'a' сравнивается с 'p'
// 'a' < 'p'
// результат: false


"2" > "12"
// строки, НЕ числа!
// сравнение посимвольно:
// '2' > '1'
// на этом всё решается
// результат: true


undefined == null
// специальное правило JS:
// undefined == null → true
// результат: true


undefined === null
// === проверяет И тип, И значение
// undefined !== null
// результат: false


null == "\n0\n"
// "\n0\n" → строка
// null == что-то → true ТОЛЬКО с undefined
// здесь строка → false
// результат: false


null === +"\n0\n"
// сначала унарный +:
// "\n0\n" → 0
// теперь: null === 0
// разные типы и значения
// результат: false

